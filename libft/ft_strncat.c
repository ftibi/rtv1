#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char			*save;
	unsigned long	i;

	save = s1;
	while (*s1)
		s1++;
	i = 0;
	while (*s2 && (i < n))
	{
		*s1 = *s2;
		s1++;
		s2++;
		i++;
	}
	*s1 = '\0';
	return (save);
}
