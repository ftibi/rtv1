#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned long i;
	unsigned char *str;

	str = s;
	i = 0;
	if (n != 0)
	{
		while (i < n)
		{
			str[i] = 0;
			i++;
		}
	}
}
